"""設定管理モジュール

ScrapingConfigデータクラスおよびライブラリ内で使用する定数を定義する。
"""

from dataclasses import dataclass, field


@dataclass
class ScrapingConfig:
    """スクレイピングの設定を保持するデータクラス

    Attributes:
        netkeiba_base_url (str): netkeibaのベースURL
        netkeiba_race_url (str): netkeibaのレースURL
        jra_url (str): JRAのURL
        headers (dict[str, str]): HTTPリクエストヘッダー
        chrome_driver_path (str): ChromeDriverのパス
        request_timeout (int): HTTPリクエストのタイムアウト(秒)
    """

    netkeiba_base_url: str = "https://db.netkeiba.com"
    netkeiba_race_url: str = "https://race.netkeiba.com"
    jra_url: str = "https://www.jra.go.jp"
    headers: dict[str, str] = field(
        default_factory=lambda: {
            "User-Agent": (
                "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                "AppleWebKit/537.36 (KHTML, like Gecko) "
                "Chrome/58.0.3029.110 Safari/537.3"
            )
        }
    )
    chrome_driver_path: str = "/usr/bin/chromedriver"
    request_timeout: int = 10


